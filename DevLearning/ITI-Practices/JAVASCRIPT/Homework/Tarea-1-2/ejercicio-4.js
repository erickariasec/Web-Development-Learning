// 4. Replica el comportamiento de los siguientes ciclos `for` utilizando ciclos `while`

// FOR LOOPS
for (let i = 0; i < 5; i++) {
    console.log("El valor de i es: " + i);
}

for (let i = 10; i >= 2; i--) {
    console.log("El valor de i es: " + i);
}


// CONVERTED TO WHILE LOOPS
let i = 0;
while (i < 5) {
    console.log("El valor de i es: " + i);
    i++;
}

let j = 10;
while (j >= 2) {
    console.log("El valor de j es: " + j);
    j--;
}