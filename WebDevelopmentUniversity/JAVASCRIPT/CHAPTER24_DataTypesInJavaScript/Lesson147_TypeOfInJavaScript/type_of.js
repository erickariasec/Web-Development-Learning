/* DATA TYPES IN JAVASCRIPT */

/* In JavaScript we say that the type of variables are DYNAMIC, since the type of data it stores could be changed at any time. */

// string data type
var personName = "Erick";
console.log(typeof personName);

personName = 2000; // Change string to number data type
console.log(typeof personName);

// numeric data type
var number = 1000;
console.log(number);

// object data type
var object = {
    name : "Juan",
    lastName : "Perez",
    phoneNumber : "0987654321"
}
console.log(typeof object);