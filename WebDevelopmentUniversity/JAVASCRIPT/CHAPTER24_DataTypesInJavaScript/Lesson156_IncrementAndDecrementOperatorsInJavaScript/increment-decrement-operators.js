let a = 3, b = 2;

// * INCREMENT

// Pre - Increment (++ operator before variable name)
z = ++a;
console.log(a);
console.log(z);

// Post - Increment (++ operator after variable name)
z = b++;
console.log(b);
console.log(z);

// * DECREMENT

// Pre - Decrement (-- operator before variable name)
z = --a;
console.log(a);
console.log(z);

// Post - Decrement (-- operator after variable name)
z = b--;
console.log(b);
console.log(z);